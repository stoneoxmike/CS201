public class Circle {
	private int x;
	private int y;
	private int r;
	
	// constructor
	public Circle() {
		x = 0;
		y = 0;
		r = 0;
	}
	
	public Circle(int x, int y, int r) {
		this.x = x;
		this.y = y;
		this.r = r;
	}
	
	// Getter and setter methods
	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}
	
	public int getR() {
		return r;
	}
}
